def forward_max_match(sentence, dictionary, max_word_length):
    index = 0
    result = []
    while index < len(sentence):
        word = None
        for size in range(max_word_length, 0, -1):
            if index + size > len(sentence):
                continue
            piece = sentence[index:index + size]
            if piece in dictionary:
                word = piece
                result.append(word)
                index += size
                break
        if word is None:
            word = sentence[index]
            result.append(word)
            index += 1
    return result


